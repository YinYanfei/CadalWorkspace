package cn.cadal.rec.tryJava;

import java.util.Arrays;

public class ArrSort {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// Analyze and Test
		double [] d = new double[5];
		
		d[0] = 0.0123;
		d[1] = 1.7820;
		d[2] = 0.2194;
		d[3] = 0.0;
		d[4] = 0.5961;
		
		Arrays.sort(d);
		
		for(int i = 0; i < d.length; ++i) {
			System.out.println(d[i]);
		}
	}

}
